package fr.maxime38.yol.utils;

public class Pair {
	
	Object a, b;
	
	public Pair(Object a, Object b) {
		this.a = a;
		this.b = b;
	}

	public Object getA() {
		return a;
	}

	public void setA(Object a) {
		this.a = a;
	}

	public Object getB() {
		return b;
	}

	public void setB(Object b) {
		this.b = b;
	}
	
	
}
